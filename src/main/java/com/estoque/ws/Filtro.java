package com.estoque.ws;

// Classe bean para representar o Filtro
public class Filtro {

	private String tipo;
	private String nome;

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
}
